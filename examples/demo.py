"""示例用法"""

from split_text.splitter import split_text, TextSplitter


def example_basic():
    """基础用法示例"""
    text = """各位朋友：
回到台北来已经二十多天，在这短短的时间里，我收到无数过去与我通信的读者、我教过的学生、以及许许多多新朋友的来信与电话。我也在台北街头看见自己的新书，挤在一大堆花花绿绿的书刊里，向我扮着顽皮的鬼脸。

每当我收到由各方面转来的你们的来信时，我在这一封封诚意的信里，才看出了我自己的形象，才知道三毛有这么多不相识的朋友在鼓励着她。

我多么希望每一封信都细细地回答你们。因为我知道，每一个写信给我的人，在提笔时，也费了番心思和时间来表示对我的关怀。

我怎么能够看见你们诚意的来信，知道你们一定在等着我的回音，而那一封封的信都如石沉大海，没有回声。

请无数写信给我的朋友了解我，三毛不是一个没有感情也没有礼貌的人。

离开家国那么久了，台北的亲情友情，整整地占据了我。我尽力愿意把我自己的时间，分给每一个关怀我的朋友。可惜的是，我一天也只能捉住二十四小时。

生活突然的忙碌热闹，使我精神上兴奋而紧张，体力上透支再透支。而内心的宁静，却已因为这些感人的真情流露起了很大的波澜。

虽然我努力在告诉自己，我要完完全全享受我在祖国的假期，游山玩水，与父母亲闲话家常。事实上，我每日的生活，已成了时间的奴隶，我日日夜夜地追赶着它，而仿佛永远不能在这件事上得到释放。"""

    result = split_text(text, max_length=60)
    
    print("=" * 60)
    print("基础用法示例")
    print("=" * 60)
    print(f"原始文本长度: {result.metadata['original_length']} 字符")
    print(f"拆分后片段数: {result.metadata['chunk_count']}")
    print()
    
    for i, chunk in enumerate(result.chunks, 1):
        print(f"[{i}] ({len(chunk)}字符)")
        print(f"    {chunk}")
        print()


